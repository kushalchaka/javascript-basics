const PI = 3.14;

if (PI === "3.14") {
    console.log("That is Pi");
} 
else {
    console.log("That is not Pi");
}


if (PI === 3.14) {
    console.log("That is really Pi");
}
else {
    console.log("That is not really Pi");
}


if (PI !== "3.14") {
    console.log("That is not Pi");
}
else {
    console.log("That is Pi");
}


if (PI !== 3.14) {
    console.log("That is not really Pi");
}
else {
    console.log("That is really Pi");
}